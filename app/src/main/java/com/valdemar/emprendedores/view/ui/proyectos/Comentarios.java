package com.valdemar.emprendedores.view.ui.proyectos;

public class Comentarios {
    private String foto;
    private String nombre;
    private String comentario;
    private String idss;
    private String id;
    private String idEmprendedor;

    public Comentarios() {
    }

    public String getFoto() {
        return foto;
    }

    public void setFoto(String foto) {
        this.foto = foto;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getComentario() {
        return comentario;
    }

    public void setComentario(String comentario) {
        this.comentario = comentario;
    }

    public String getIdss() {
        return idss;
    }

    public void setIdss(String idss) {
        this.idss = idss;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getIdEmprendedor() {
        return idEmprendedor;
    }

    public void setIdEmprendedor(String idEmprendedor) {
        this.idEmprendedor = idEmprendedor;
    }
}
